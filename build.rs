use std::fs;
use std::path::Path;

fn main() {
    // Create icons directory
    fs::create_dir_all("icons").ok();
    
    let icon_path = "icons/icon.ico";
    
    // Create a minimal valid 32x32 ICO file if it doesn't exist
    if !Path::new(icon_path).exists() {
        // This is a valid 32x32 transparent ICO file
        let ico_bytes = vec![
            0x00, 0x00, 0x01, 0x00, 0x01, 0x00, 0x20, 0x20, 0x00, 0x00, 0x01, 0x00, 0x20, 0x00,
            0x88, 0x04, 0x00, 0x00, 0x16, 0x00, 0x00, 0x00, 0x28, 0x00, 0x00, 0x00, 0x20, 0x00,
            0x00, 0x00, 0x40, 0x00, 0x00, 0x00, 0x01, 0x00, 0x20, 0x00, 0x00, 0x00, 0x00, 0x00,
            0x00, 0x04, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        ];
        
        let mut full_ico = ico_bytes.clone();
        // Add padding to make it a valid 32x32 image (1024 bytes for RGBA)
        full_ico.extend(vec![0u8; 1024 - ico_bytes.len()]);
        
        fs::write(icon_path, full_ico).ok();
    }
    
    tauri_build::build()
}
